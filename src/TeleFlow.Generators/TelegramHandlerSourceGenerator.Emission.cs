    private static void AppendCallbackDataCodec(
        StringBuilder builder,
        GeneratedCallbackDataPayload payload,
        int index)
    {
        builder.AppendLine($"    private static string PackCallbackData_{index}(object payload)");
        builder.AppendLine("    {");
        builder.AppendLine($"        var value = ({payload.TypeName})payload;");
        builder.AppendLine("        return string.Concat(");
        builder.AppendLine($"            {ToLiteral(payload.Prefix)}");

        foreach (GeneratedCallbackDataField field in payload.Fields)
        {
            builder.AppendLine("            ,");
            builder.Append("            \":\", ");
            builder.Append(GetCallbackDataPackExpression(field));
            builder.AppendLine();
        }

        builder.AppendLine("        );");
        builder.AppendLine("    }");
        builder.AppendLine();

        builder.AppendLine($"    private static bool MatchesCallbackData_{index}(string serializedPayload)");
        builder.AppendLine("    {");
        builder.AppendLine($"        return MatchesCallbackData(serializedPayload, {ToLiteral(payload.Prefix)}, {payload.Fields.Length});");
        builder.AppendLine("    }");
        builder.AppendLine();

        builder.AppendLine($"    private static object UnpackCallbackData_{index}(string serializedPayload)");
        builder.AppendLine("    {");
        builder.AppendLine(
            $"        var parts = SplitCallbackData(serializedPayload, {ToLiteral(payload.Prefix)}, {payload.Fields.Length}, {ToLiteral(payload.TypeMetadataName)});");

        if (payload.UsesConstructor)
        {
            builder.Append($"        return new {payload.TypeName}(");

            for (int fieldIndex = 0; fieldIndex < payload.Fields.Length; fieldIndex++)
            {
                if (fieldIndex > 0)
                {
                    builder.Append(", ");
                }

                builder.Append(GetCallbackDataUnpackExpression(payload, payload.Fields[fieldIndex], fieldIndex + 1));
            }

            builder.AppendLine(");");
        }
        else
        {
            builder.AppendLine($"        return new {payload.TypeName}");
            builder.AppendLine("        {");

            for (int fieldIndex = 0; fieldIndex < payload.Fields.Length; fieldIndex++)
            {
                GeneratedCallbackDataField field = payload.Fields[fieldIndex];
                builder.AppendLine($"            {field.PropertyName} = {GetCallbackDataUnpackExpression(payload, field, fieldIndex + 1)},");
            }

            builder.AppendLine("        };");
        }

        builder.AppendLine("    }");
        builder.AppendLine();
    }

    private static string GetCallbackDataPackExpression(GeneratedCallbackDataField field)
    {
        string property = $"value.{field.PropertyName}";

        return field.Kind switch
        {
            GeneratedCallbackDataFieldKind.String => $"EscapeCallbackDataField({property})",
            GeneratedCallbackDataFieldKind.Int32 or
                GeneratedCallbackDataFieldKind.Int64 => $"{property}.ToString(global::System.Globalization.CultureInfo.InvariantCulture)",
            GeneratedCallbackDataFieldKind.Boolean => $"{property} ? \"true\" : \"false\"",
            GeneratedCallbackDataFieldKind.Enum => $"EscapeCallbackDataField({property}.ToString())",
            _ => throw new InvalidOperationException($"Unsupported generated callback data field kind '{field.Kind}'.")
        };
    }

    private static string GetCallbackDataUnpackExpression(
        GeneratedCallbackDataPayload payload,
        GeneratedCallbackDataField field,
        int partIndex)
    {
        string value = $"UnescapeCallbackDataField(parts[{partIndex}])";

        return field.Kind switch
        {
            GeneratedCallbackDataFieldKind.String => value,
            GeneratedCallbackDataFieldKind.Int32 => $"global::System.Int32.Parse({value}, global::System.Globalization.CultureInfo.InvariantCulture)",
            GeneratedCallbackDataFieldKind.Int64 => $"global::System.Int64.Parse({value}, global::System.Globalization.CultureInfo.InvariantCulture)",
            GeneratedCallbackDataFieldKind.Boolean => $"global::System.Boolean.Parse({value})",
            GeneratedCallbackDataFieldKind.Enum => $"ParseCallbackDataEnum<{field.TypeName}>({value}, {ToLiteral(field.PropertyName)}, {ToLiteral(payload.TypeMetadataName)})",
            _ => throw new InvalidOperationException($"Unsupported generated callback data field kind '{field.Kind}'.")
        };
    }

    private static void AppendCallbackDataHelpers(StringBuilder builder)
    {
        builder.AppendLine("    private static bool MatchesCallbackData(string serializedPayload, string prefix, int fieldCount)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (!serializedPayload.StartsWith(prefix, global::System.StringComparison.Ordinal))");
        builder.AppendLine("        {");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (fieldCount == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return serializedPayload.Length == prefix.Length;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (serializedPayload.Length <= prefix.Length || serializedPayload[prefix.Length] != ':')");
        builder.AppendLine("        {");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var separatorCount = 0;");
        builder.AppendLine();
        builder.AppendLine("        for (var index = prefix.Length; index < serializedPayload.Length; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (serializedPayload[index] == ':')");
        builder.AppendLine("            {");
        builder.AppendLine("                separatorCount++;");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return separatorCount == fieldCount;");
        builder.AppendLine("    }");
        builder.AppendLine();

        builder.AppendLine("    private static string[] SplitCallbackData(string serializedPayload, string prefix, int fieldCount, string payloadTypeName)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (!MatchesCallbackData(serializedPayload, prefix, fieldCount))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw new global::System.Text.Json.JsonException($\"Telegram callback data does not match payload type '{payloadTypeName}'.\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return serializedPayload.Split(':');");
        builder.AppendLine("    }");
        builder.AppendLine();

        builder.AppendLine("    private static string EscapeCallbackDataField(string? value)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (value is null)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw new global::System.InvalidOperationException(\"Compact callback data fields must not be null.\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return value");
        builder.AppendLine("            .Replace(\"%\", \"%25\", global::System.StringComparison.Ordinal)");
        builder.AppendLine("            .Replace(\":\", \"%3A\", global::System.StringComparison.Ordinal);");
        builder.AppendLine("    }");
        builder.AppendLine();

        builder.AppendLine("    private static string UnescapeCallbackDataField(string value)");
        builder.AppendLine("    {");
        builder.AppendLine("        return value");
        builder.AppendLine("            .Replace(\"%3A\", \":\", global::System.StringComparison.Ordinal)");
        builder.AppendLine("            .Replace(\"%25\", \"%\", global::System.StringComparison.Ordinal);");
        builder.AppendLine("    }");
        builder.AppendLine();

        builder.AppendLine("    private static TEnum ParseCallbackDataEnum<TEnum>(string value, string fieldName, string payloadTypeName)");
        builder.AppendLine("        where TEnum : struct, global::System.Enum");
        builder.AppendLine("    {");
        builder.AppendLine("        if (global::System.Enum.TryParse<TEnum>(value, ignoreCase: false, out var result))");
        builder.AppendLine("        {");
        builder.AppendLine("            return result;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        throw new global::System.Text.Json.JsonException($\"Telegram callback data field '{fieldName}' on payload type '{payloadTypeName}' is not a valid {typeof(TEnum).FullName} value.\");");
        builder.AppendLine("    }");
        builder.AppendLine();
    }

    private static void AppendInvoker(StringBuilder builder, GeneratedHandlerMethod handler, int index)
    {
        builder.AppendLine($"    private static async global::System.Threading.Tasks.ValueTask Invoke_{index}(");
        builder.AppendLine("        global::System.IServiceProvider services,");
        builder.AppendLine("        object?[] arguments,");
        builder.AppendLine("        global::System.Threading.CancellationToken cancellationToken)");
        builder.AppendLine("    {");
        builder.AppendLine(
            $"        var handler = ({handler.HandlerTypeName})(services.GetService(typeof({handler.HandlerTypeName})) ?? throw new global::System.InvalidOperationException({ToLiteral($"Telegram handler '{handler.HandlerTypeMetadataName}' is not registered in the current service provider.")}));");
        builder.Append("        await handler.");
        builder.Append(handler.MethodName);
        builder.Append('(');

        for (int parameterIndex = 0; parameterIndex < handler.Parameters.Length; parameterIndex++)
        {
            if (parameterIndex > 0)
            {
                builder.Append(", ");
            }

            GeneratedParameter parameter = handler.Parameters[parameterIndex];
            builder.Append('(');
            builder.Append(parameter.TypeName);
            builder.Append($")arguments[{parameterIndex}]!");
        }

        builder.AppendLine(").ConfigureAwait(false);");
        builder.AppendLine("    }");
        builder.AppendLine();
    }

    private static void AppendErrorInvoker(StringBuilder builder, GeneratedErrorHandlerMethod handler, int index)
    {
        string asyncKeyword = handler.ReturnKind == GeneratedErrorReturnKind.Sync ? string.Empty : "async ";

        builder.AppendLine($"    private static {asyncKeyword}global::System.Threading.Tasks.ValueTask<global::TeleFlow.Telegram.TelegramErrorHandlingResult> InvokeError_{index}(");
        builder.AppendLine("        global::System.IServiceProvider services,");
        builder.AppendLine("        object?[] arguments,");
        builder.AppendLine("        global::System.Threading.CancellationToken cancellationToken)");
        builder.AppendLine("    {");
        builder.AppendLine(
            $"        var handler = ({handler.HandlerTypeName})(services.GetService(typeof({handler.HandlerTypeName})) ?? throw new global::System.InvalidOperationException({ToLiteral($"Telegram error handler '{handler.HandlerTypeMetadataName}' is not registered in the current service provider.")}));");

        if (handler.ReturnKind == GeneratedErrorReturnKind.Sync)
        {
            builder.Append("        return new global::System.Threading.Tasks.ValueTask<global::TeleFlow.Telegram.TelegramErrorHandlingResult>(handler.");
            AppendErrorInvocationArguments(builder, handler);
            builder.AppendLine(");");
        }
        else
        {
            builder.Append("        return await handler.");
            AppendErrorInvocationArguments(builder, handler);
            builder.AppendLine(".ConfigureAwait(false);");
        }

        builder.AppendLine("    }");
        builder.AppendLine();
    }

    private static void AppendErrorInvocationArguments(
        StringBuilder builder,
        GeneratedErrorHandlerMethod handler)
    {
        builder.Append(handler.MethodName);
        builder.Append('(');

        for (int parameterIndex = 0; parameterIndex < handler.Parameters.Length; parameterIndex++)
        {
            if (parameterIndex > 0)
            {
                builder.Append(", ");
            }

            GeneratedErrorParameter parameter = handler.Parameters[parameterIndex];
            builder.Append('(');
            builder.Append(parameter.TypeName);
            builder.Append($")arguments[{parameterIndex}]!");
        }

        builder.Append(')');
    }

    private static string ToNullableLiteral(string? value)
    {
        return value is null ? "null" : ToLiteral(value);
    }

    private static string ToTypeOfNullable(string? typeName)
    {
        return typeName is null ? "null" : $"typeof({typeName})";
    }

    private static string ToLiteral(string value)
    {
        StringBuilder builder = new StringBuilder(value.Length + 2);
        builder.Append('"');

        foreach (char character in value)
        {
            builder.Append(character switch
            {
                '\\' => "\\\\",
                '"' => "\\\"",
                '\r' => "\\r",
                '\n' => "\\n",
                '\t' => "\\t",
                _ => character.ToString()
            });
        }

        builder.Append('"');
        return builder.ToString();
    }

    private static string ToBoolLiteral(bool value)
    {
        return value ? "true" : "false";
    }

    private static string ToAttributeCreationExpression(AttributeData attribute)
    {
        if (attribute.AttributeClass is null)
        {
            return "null!";
        }

        StringBuilder builder = new StringBuilder();
        builder.Append("new ");
        builder.Append(attribute.AttributeClass.ToDisplayString(FullyQualifiedFormat));
        builder.Append('(');

        for (int index = 0; index < attribute.ConstructorArguments.Length; index++)
        {
            if (index > 0)
            {
                builder.Append(", ");
            }

            builder.Append(ToAttributeArgumentExpression(attribute.ConstructorArguments[index]));
        }

        builder.Append(')');

        if (attribute.NamedArguments.Length > 0)
        {
            builder.Append(" { ");

            for (int index = 0; index < attribute.NamedArguments.Length; index++)
            {
                if (index > 0)
                {
                    builder.Append(", ");
                }

                KeyValuePair<string, TypedConstant> argument = attribute.NamedArguments[index];
                builder.Append(argument.Key);
                builder.Append(" = ");
                builder.Append(ToAttributeArgumentExpression(argument.Value));
            }

            builder.Append(" }");
        }

        return builder.ToString();
    }

    private static string ToAttributeArgumentExpression(TypedConstant constant)
    {
        if (constant.IsNull)
        {
            return "null";
        }

        return constant.Kind switch
        {
            TypedConstantKind.Primitive => ToPrimitiveLiteral(constant.Value, constant.Type),
            TypedConstantKind.Enum => ToEnumLiteral(constant),
            TypedConstantKind.Type => constant.Value is ITypeSymbol type
                ? $"typeof({type.ToDisplayString(FullyQualifiedFormat)})"
                : "null",
            TypedConstantKind.Array => ToArrayLiteral(constant),
            _ => "null"
        };
    }

    private static string ToPrimitiveLiteral(
        object? value,
        ITypeSymbol? type)
    {
        return value switch
        {
            string stringValue => ToLiteral(stringValue),
            char charValue => ToCharLiteral(charValue),
            bool boolValue => ToBoolLiteral(boolValue),
            byte byteValue => byteValue.ToString(System.Globalization.CultureInfo.InvariantCulture),
            sbyte sbyteValue => sbyteValue.ToString(System.Globalization.CultureInfo.InvariantCulture),
            short shortValue => shortValue.ToString(System.Globalization.CultureInfo.InvariantCulture),
            ushort ushortValue => ushortValue.ToString(System.Globalization.CultureInfo.InvariantCulture),
            int intValue => intValue.ToString(System.Globalization.CultureInfo.InvariantCulture),
            uint uintValue => uintValue.ToString(System.Globalization.CultureInfo.InvariantCulture) + "U",
            long longValue => longValue.ToString(System.Globalization.CultureInfo.InvariantCulture) + "L",
            ulong ulongValue => ulongValue.ToString(System.Globalization.CultureInfo.InvariantCulture) + "UL",
            float floatValue => floatValue.ToString("R", System.Globalization.CultureInfo.InvariantCulture) + "F",
            double doubleValue => doubleValue.ToString("R", System.Globalization.CultureInfo.InvariantCulture) + "D",
            _ when type?.SpecialType == SpecialType.System_Object => "null",
            _ => value?.ToString() ?? "null"
        };
    }

    private static string ToCharLiteral(char value)
    {
        string escaped = value switch
        {
            '\'' => "\\'",
            '\\' => "\\\\",
            '\r' => "\\r",
            '\n' => "\\n",
            '\t' => "\\t",
            _ => value.ToString()
        };

        return $"'{escaped}'";
    }

    private static string ToEnumLiteral(TypedConstant constant)
    {
        var enumType = constant.Type?.ToDisplayString(FullyQualifiedFormat) ?? "int";
        var value = constant.Value switch
        {
            ulong ulongValue => ulongValue.ToString(System.Globalization.CultureInfo.InvariantCulture) + "UL",
            long longValue => longValue.ToString(System.Globalization.CultureInfo.InvariantCulture) + "L",
            uint uintValue => uintValue.ToString(System.Globalization.CultureInfo.InvariantCulture) + "U",
            _ => Convert.ToString(constant.Value, System.Globalization.CultureInfo.InvariantCulture) ?? "0"
        };

        return $"({enumType}){value}";
    }

    private static string ToArrayLiteral(TypedConstant constant)
    {
        var elementType = constant.Type is IArrayTypeSymbol arrayType
            ? arrayType.ElementType.ToDisplayString(FullyQualifiedFormat)
            : "object";
        StringBuilder builder = new StringBuilder();

        builder.Append("new ");
        builder.Append(elementType);
        builder.Append("[] { ");

        for (int index = 0; index < constant.Values.Length; index++)
        {
            if (index > 0)
            {
                builder.Append(", ");
            }

            builder.Append(ToAttributeArgumentExpression(constant.Values[index]));
        }

        builder.Append(" }");

        return builder.ToString();
    }
